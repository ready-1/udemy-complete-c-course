#include<stdio.h>
#include<stdlib.h>

int main() {

    // addition
    printf("%d + %d = %d\n", 5, 2, (5 + 2));

    // subtraction
    printf("%d - %d = %d\n", 5, 2, (5 - 2));
    
    // multiplication
    printf("%d x %d = %d\n", 5, 2, (5 * 2));
    
    // division
    printf("%d / %d = %d\n", 5, 2, (5/2));
    
    // mod
    printf("%d %% %d = %d\n", 5, 2, (5%2));

    return 0;
}